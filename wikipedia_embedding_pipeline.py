    if part in processed:
        print(f"\n========== SKIP PART (already done): {part} ==========\n")
        continue

    print(f"\n========== PROCESSING PART: {part} ==========\n")
    total_added_this_part = 0

    # loop AA, AB, AC, ...
    for sub in sorted(os.listdir(part_path)):
        sub_path = os.path.join(part_path, sub)
        if not os.path.isdir(sub_path):
            continue

        # loop wiki_0.jsonl, wiki_1.jsonl, ...
        for fname in sorted(os.listdir(sub_path)):
            if fname.endswith(".jsonl"):
                fp = os.path.join(sub_path, fname)
                total_added_this_part += process_jsonl_file(fp)

    print(f"\n>>> Part {part} finished, added {total_added_this_part} vectors")

    # -------- SAVE ONCE PER PART --------
    safe_save_faiss(index, FAISS_PATH)
    atomic_pickle_dump(metadata, META_PATH)
    atomic_pickle_dump(uids, UID_PATH)

    # mark part as processed
    processed.add(part)
    atomic_pickle_dump(processed, PROCESSED_PATH)

    grand_total += total_added_this_part

