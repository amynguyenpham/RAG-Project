import pickle, os, shutil, faiss, tempfile

def atomic_pickle_dump(obj, final_path):
    """Write pickle safely by writing to temp file then atomic rename."""
    dir_name = os.path.dirname(final_path)
    fd, tmp_path = tempfile.mkstemp(dir=dir_name)
    os.close(fd)
    try:
        with open(tmp_path, "wb") as f:
            pickle.dump(obj, f, protocol=pickle.HIGHEST_PROTOCOL)
        os.replace(tmp_path, final_path)  # atomic
    except Exception as e:
        print("Atomic pickle save failed:", e)
        if os.path.exists(tmp_path):
            os.remove(tmp_path)
        raise


def safe_save_faiss(index, final_path):
    """Ensure FAISS index is safely written using temp + rename."""
    dir_name = os.path.dirname(final_path)
    tmp_path = os.path.join(dir_name, "faiss_tmp.index")

    # Write to local tmp file
    faiss.write_index(index, tmp_path)

    # Atomic move (safe even on Google Drive)
    os.replace(tmp_path, final_path)

    os.sync()  # force filesystem sync

def process_jsonl_file(jsonl_path):
    global start_uid, index, metadata, uids

    print("  File:", jsonl_path)
    count_added = 0
    texts = []
    local_meta = []

    with open(jsonl_path, "r", encoding="utf-8") as f:
        for line in f:
            try:
                item = json.loads(line.strip())
            except:
                continue

            content = item.get("Content", "").strip()
            if not content:
                continue

            uid = start_uid
            start_uid += 1

            texts.append(content)
            local_meta.append({
                "uid": uid,
                "title": item.get("Title"),
                "section": item.get("Section"),
                "content": item.get("Content")
            })
            uids.append(uid)

            # Batch embed
            if len(texts) >= BATCH_SIZE:
                # embs = model.encode(texts, convert_to_numpy=True,
                #                     normalize_embeddings=True)
                embs = model.encode(
                    texts,
                    batch_size=BATCH_SIZE,
                    device="cuda",
                    convert_to_numpy=True,
                    show_progress_bar=False,
                    normalize_embeddings=True
                )
                index.add(embs.astype(np.float32))
                metadata.extend(local_meta)
                count_added += len(embs)
                texts, local_meta = [], []

    # leftover
    if texts:
        embs = model.encode(texts, convert_to_numpy=True,
                            normalize_embeddings=True)
        index.add(embs.astype(np.float32))
        metadata.extend(local_meta)
        count_added += len(embs)

    return count_added

grand_total = 0

